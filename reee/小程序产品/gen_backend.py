# ─────────── 样式工具 ───────────

def set_cell_bg(cell, hex_color):
    tc = cell._tc
    tcPr = tc.get_or_add_tcPr()
    shd = OxmlElement("w:shd")
    shd.set(qn("w:val"), "clear")
    shd.set(qn("w:color"), "auto")
    shd.set(qn("w:fill"), hex_color)
    tcPr.append(shd)

def add_heading(doc, text, level=1, color="1F4E79"):
    p = doc.add_heading(text, level=level)
    p.alignment = WD_ALIGN_PARAGRAPH.LEFT
    for run in p.runs:
        run.font.color.rgb = RGBColor.from_string(color)
    return p

def create_doc(title, subtitle, version="V1.0", date="2026-06-06"):
    doc = Document()
    section = doc.sections[0]
    section.left_margin = Cm(1.8)
    section.right_margin = Cm(1.8)
    section.top_margin = Cm(1.5)
    section.bottom_margin = Cm(1.5)

    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = p.add_run(title)
    run.font.size = Pt(22)
    run.font.bold = True
    run.font.color.rgb = RGBColor.from_string("1F4E79")

    p2 = doc.add_paragraph()
    p2.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run2 = p2.add_run(subtitle)
    run2.font.size = Pt(12)
    run2.font.color.rgb = RGBColor.from_string("595959")

    doc.add_paragraph()

    info_table = doc.add_table(rows=5, cols=4)
    info_table.style = "Table Grid"
    info_data = [
        ("产品名称", "睿联盟", "版本", version),
        ("端类型", "后台管理（机构端）", "创建日期", date),
        ("测试类型", "功能测试 + 界面测试", "测试阶段", "设计稿阶段"),
        ("覆盖设计稿", "46 张截图（睿联盟-后台）", "编写人", "Claude"),
        ("审核人", "待定", "用例总数", "待统计"),
    ]
    for r_idx, row_data in enumerate(info_data):
        for c_idx, val in enumerate(row_data):
            info_table.rows[r_idx].cells[c_idx].text = val
            if c_idx % 2 == 0:
                set_cell_bg(info_table.rows[r_idx].cells[c_idx], "DEEAF1")
                for p_cell in info_table.rows[r_idx].cells[c_idx].paragraphs:
                    for run in p_cell.runs:
                        run.font.bold = True
                        run.font.size = Pt(9)
            else:
                for p_cell in info_table.rows[r_idx].cells[c_idx].paragraphs:
                    for run in p_cell.runs:
                        run.font.size = Pt(9)

    info_table.columns[0].width = Cm(3)
    info_table.columns[1].width = Cm(5.5)
    info_table.columns[2].width = Cm(3)
    info_table.columns[3].width = Cm(5.5)
    doc.add_paragraph()
    return doc

def add_module_section(doc, module_name, module_summary=""):
    if module_summary:
        p = doc.add_paragraph()
        run = p.add_run("  说明：" + module_summary)
        run.font.size = Pt(9)
        run.font.color.rgb = RGBColor.from_string("7F7F7F")
    add_heading(doc, "■ " + module_name, level=2)

def build_test_table(doc, headers):
    table = doc.add_table(rows=1, cols=len(headers))
    table.style = "Table Grid"
    table.alignment = WD_TABLE_ALIGNMENT.CENTER
    hdr_cells = table.rows[0].cells
    for i, h in enumerate(headers):
        hdr_cells[i].text = h
        set_cell_bg(hdr_cells[i], "2E75B6")
        for p_cell in hdr_cells[i].paragraphs:
            p_cell.alignment = WD_ALIGN_PARAGRAPH.CENTER
            for run in p_cell.runs:
                run.font.bold = True
                run.font.color.rgb = RGBColor(255, 255, 255)
                run.font.size = Pt(9)
    return table

def add_test_row(table, module, case_id, case_name, precond, test_step, expected, priority, note=""):
    row = table.add_row()
    cells = row.cells
    data = [module, case_id, case_name, precond, test_step, expected, priority, note]
    for i, val in enumerate(data):
        cells[i].text = val
        for p_cell in cells[i].paragraphs:
            for run in p_cell.runs:
                run.font.size = Pt(8.5)


