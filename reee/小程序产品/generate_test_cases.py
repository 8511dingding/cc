# ─────────── 构建文档 ───────────

HEADERS = ["所属模块", "用例编号", "用例名称", "前置条件", "测试步骤", "预期结果", "优先级", "备注"]

def build_doc(doc, modules):
    for mod in modules:
        add_module_section(doc, mod["name"], mod["summary"])
        table = build_test_table(doc, HEADERS)
        for case in mod["cases"]:
            case_id, case_name, precond, test_step, expected, priority = case
            module_short = mod["name"].split("、")[1].split("（")[0] if "、" in mod["name"] else mod["name"]
            add_test_row(table, module_short, case_id, case_name, precond, test_step, expected, priority)
        doc.add_page_break()

    total = sum(len(m["cases"]) for m in modules)
    p = doc.add_paragraph()
    run = p.add_run("共 " + str(len(modules)) + " 个模块，" + str(total) + " 条测试用例")
    run.font.size = Pt(9)
    run.font.italic = True
    run.font.color.rgb = RGBColor.from_string("7F7F7F")


